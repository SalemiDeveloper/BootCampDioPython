saldo = 450
saque = 250

#IGUALDADE
print(saldo == saque) #vai retornar "false"

#DIFERENÃ‡A
print(saldo != saque) #vai retornar "true"

#MAIOR QUE / MAIOR OU IGUAL
print(saldo > saque) #vai retornar "true"
print(saldo >= saque) #vai retornar "true"

#MENOR QUE / MENOR OU IGUAL
print(saldo < saque) #vai retornar "false"
print(saldo <= saque) #vai retornar "false"





